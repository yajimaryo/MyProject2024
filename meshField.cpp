void MeshField::Draw()
{
	// 入力レイアウト設定
	Renderer::GetDeviceContext()->IASetInputLayout(m_VertexLayout);

	// シェーダ設定
	Renderer::GetDeviceContext()->VSSetShader(m_VertexShader, NULL, 0);
	Renderer::GetDeviceContext()->PSSetShader(m_PixelShader, NULL, 0);

	PARAMETER param;
	param.baseColor = D3DXCOLOR(0.0f, 0.0f, 100.0f, 1.0f);
	param.diffColor.r = 1.0f;
	Renderer::SetParameter(param);


	// マトリクス設定
	D3DXMATRIX world, scale, rot, trans;
	D3DXMatrixScaling(&scale, m_Scale.x, m_Scale.y, m_Scale.z);
	D3DXMatrixRotationYawPitchRoll(&rot, m_Rotation.y, m_Rotation.x, m_Rotation.z);
	D3DXMatrixTranslation(&trans, m_Position.x, m_Position.y, m_Position.z);
	world = scale * rot * trans;
	Renderer::SetWorldMatrix(&world);


	// 頂点バッファ設定
	UINT stride = sizeof(VERTEX_3D);
	UINT offset = 0;
	Renderer::GetDeviceContext()->IASetVertexBuffers(0, 1, &m_VertexBuffer, &stride, &offset);

	// インデックスバッファ設定
	Renderer::GetDeviceContext()->IASetIndexBuffer(m_IndexBuffer, DXGI_FORMAT_R32_UINT, 0);

	// マテリアル設定
	MATERIAL material;
	ZeroMemory(&material, sizeof(material));
	material.Diffuse = D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f);
	material.TextureEnable = true;
	Renderer::SetMaterial(material);

	// テクスチャ設定
	ID3D11ShaderResourceView* pTextureView = ResourceManager::GetInstance()->GetResouseTexture(m_TerrainTextureName);
	Renderer::GetDeviceContext()->PSSetShaderResources(0, 1, &pTextureView);

	ID3D11ShaderResourceView* pTextureR = ResourceManager::GetInstance()->GetResouseTexture(m_RTextureName);
	Renderer::GetDeviceContext()->PSSetShaderResources(1, 1, &pTextureR);

	ID3D11ShaderResourceView* pTextureG = ResourceManager::GetInstance()->GetResouseTexture(m_GTextureName);
	Renderer::GetDeviceContext()->PSSetShaderResources(2, 1, &pTextureG);

	ID3D11ShaderResourceView* pTextureB = ResourceManager::GetInstance()->GetResouseTexture(m_BTextureName);
	Renderer::GetDeviceContext()->PSSetShaderResources(3, 1, &pTextureB);

	// プリミティブトポロジ設定
	Renderer::GetDeviceContext()->IASetPrimitiveTopology(D3D11_PRIMITIVE_TOPOLOGY_TRIANGLESTRIP);

	// ポリゴン描画
	Renderer::GetDeviceContext()->DrawIndexed(((m_XNum + 2) * 2) * (m_ZNum - 2), 0, 0);

}


float MeshField::GetHeight(D3DXVECTOR3 Position)
{
	if (this == nullptr)
	{
		return 0.0f;
	}
	int x, z;

	if (Position.x > 100.0f)
		Position.x = 99.0f;

	if (Position.x < -100.0f)
		Position.x = -99.0f;

	if (Position.z > 100.0f)
		Position.z = 99.0f;

	if (Position.z < -100.0f)
		Position.z = -99.0f;

	//ブロック番号算出
	x = Position.x / 10.0f + (m_XNum / 2);
	z = Position.z / -10.0f + (m_ZNum / 2);

	D3DXVECTOR3 pos0, pos1, pos2, pos3;

	pos0 = m_Vertex[x][z].Position;
	pos1 = m_Vertex[x + 1][z].Position;
	pos2 = m_Vertex[x][z + 1].Position;
	pos3 = m_Vertex[x + 1][z + 1].Position;

	D3DXVECTOR3 v12, v1p, c;
	v12 = pos2 - pos1;
	v1p = Position - pos1;

	D3DXVec3Cross(&c, &v12, &v1p);

	float py;
	D3DXVECTOR3 n;
	if (c.y > 0.0f)
	{
		//左上
		D3DXVECTOR3 v10;
		v10 = pos0 - pos1;
		D3DXVec3Cross(&n, &v10, &v12);
	}
	else
	{
		//右下
		D3DXVECTOR3 v13;
		v13 = pos3 - pos1;
		D3DXVec3Cross(&n, &v12, &v13);
	}

	py = -((Position.x - pos1.x) * n.x + (Position.z - pos1.z) * n.z) / n.y + pos1.y;

	return py;
}