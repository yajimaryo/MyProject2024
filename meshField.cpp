void MeshField::Init(int FieldNum, char* TerrainTex, char* RTex, char* GTex, char*BTex)
{
	m_XNum = 30;
	m_ZNum = 30;

	float height[31][31];

	for (int x = 0; x < 31; x++)
	{
		for (int z = 0; z < 31; z++)
		{
			if (FieldNum == 0)
			{
				height[x][z] = g_FieldHeight[x][z];
			}
			else
			{
				height[x][z] = g_FieldTown[x][z];
			}
		}
	}

	// 頂点バッファ生成
	{
		for (int x = 0; x <= m_XNum; x++)
		{
			for (int z = 0; z <= m_ZNum; z++)
			{
				m_Vertex[x][z].Position = D3DXVECTOR3((x - (m_XNum / 2)) * 10.0f, height[z][x], (z - (m_ZNum / 2)) * -10.0f);
				m_Vertex[x][z].Normal = D3DXVECTOR3(0.0f, 1.0f, 0.0f);//法線ベクトル
				m_Vertex[x][z].Diffuse = D3DXVECTOR4(1.0f, 1.0f, 1.0f, 1.0f);
				m_Vertex[x][z].TexCoord = D3DXVECTOR2(x * 0.5f, z * 0.5f);
			}
		}
		for (int x = 1; x <= m_XNum - 1; x++)
		{
			for (int z = 1; z <= m_ZNum - 1; z++)
			{
				D3DXVECTOR3 vx, vz, vn;
				vx = m_Vertex[x + 1][z].Position - m_Vertex[x - 1][z].Position;
				vz = m_Vertex[x][z - 1].Position - m_Vertex[x][z + 1].Position;
				D3DXVec3Cross(&vn, &vz, &vx);
				D3DXVec3Normalize(&vn, &vn);
				m_Vertex[x][z].Normal = vn;
			}
		}

		D3D11_BUFFER_DESC bd;
		ZeroMemory(&bd, sizeof(bd));
		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(VERTEX_3D) * (m_XNum + 1) * (m_ZNum + 1);
		bd.BindFlags = D3D11_BIND_VERTEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = m_Vertex;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_VertexBuffer);
	}




	// インデックスバッファ生成
	{
		//unsigned int index[((20 + 2) * 2) * 20 - 2];
		unsigned int* index = new unsigned int[((m_XNum + 2) * 2) * m_ZNum - 2];

		int i = 0;
		for (int x = 0; x < m_XNum; x++)
		{
			for (int z = 0; z < m_ZNum + 1; z++)
			{
				index[i] = x * (m_XNum + 1) + z;
				i++;

				index[i] = (x + 1) * (m_XNum + 1) + z;
				i++;
			}

			if (x == (m_XNum - 1))
				break;

			index[i] = (x + 1) * (m_XNum + 1) + m_XNum;
			i++;

			index[i] = (x + 1) * (m_XNum + 1);
			i++;
		}

		D3D11_BUFFER_DESC bd;
		ZeroMemory(&bd, sizeof(bd));
		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(unsigned int) * (((m_XNum + 2) * 2) * m_ZNum - 2);
		bd.BindFlags = D3D11_BIND_INDEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = index;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_IndexBuffer);

		// メモリの解放
		delete[] index;
	}

	m_TerrainTextureName = TerrainTex;
	m_RTextureName = RTex;
	m_GTextureName = GTex;
	m_BTextureName = BTex;

	Renderer::CreateVertexShader(&m_VertexShader, &m_VertexLayout, "shader\\groundVS.cso");

	Renderer::CreatePixelShader(&m_PixelShader, "shader\\groundPS.cso");
}


